<h3>Welcome to Feednews!</h3>
<p>To get started, hit the follow button on any of the ready to follow rss feeds. After hitting follow, you can refresh the page to have this sidebar populated with newsitems.</p>

<p>If you want to add your own feeds, hit the cog icon in the top-right, or select settings in the menu that you can find when clicking on your name in the top-right corner of the screen.</p>

<p>Please note that this site is currently in active development and the out-of-the box experience isn't the way I would like it yet. If you have any questions, let me know on github.</p>
